#include "../include/push_swap.h"

static int count_split_args(char **args)
{
    int count;

    count = 0;
    if (!args)
        return (0);
    while (args[count] != NULL)
        count++;
    return (count);
}

void free_split_args(char **split_args)
{
    int i;

    i = 0;
    while (split_args[i] != NULL)
    {
        free(split_args[i]);
        i++;
    }
    free(split_args);
}

char **handle_str(char *arg, int *new_argc)
{
    char **split_args;

    split_args = ft_split(arg, ' ');
    if (!split_args)
    {
        ft_putstr_fd("Error: Failed to split arguments\n", 2);
        exit(1);
    }
    *new_argc = count_split_args(split_args);
    return (split_args);
}

